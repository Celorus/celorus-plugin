#!/usr/bin/env node
// Celorus Design System — composition lint (guides/COMPOSITION.md is the prose contract)
//
// Tokens have hard gates (build.mjs contrast gate, hex policy); COMPOSITION is what
// actually carries the brand and until this file had none. This lint makes the
// anatomy contract machine-checkable per SURFACE CLASS.
//
// Every HTML surface declares its class in <head>:
//   <meta name="celorus-surface" content="collateral|command|tier1-artifact|tier2-embedded|app|deck|marketing|banner|email">
//
// Usage: node tools/composition-lint.mjs <file.html> [more.html ...]
// Exit: non-zero if any ERROR (warnings never fail the build).
//
// Zero dependencies, same posture as tokens/build.mjs. Checks are regex heuristics
// by design — cheap, explainable, and good enough to stop the failure classes we
// have actually shipped (2026-07-08 audit): missing theme bridge, missing provenance,
// full-page artifacts stripped of the charcoal topbar, hex literals smuggled into
// <style>, micro-type creep.

import { readFileSync } from "node:fs";

const CLASSES = ["collateral","command","tier1-artifact","tier2-embedded","app","deck","marketing","banner","email"];

// requirement matrix: E = error when missing, W = warn when missing,
// "forbidden" = warn when PRESENT (tier-2 must not replicate host chrome).
const RULES = {
  bridge:    { collateral:"E", command:"E", "tier1-artifact":"E", "tier2-embedded":"E", app:"E", deck:"E", marketing:"E", banner:"-", email:"-" },
  provenance:{ collateral:"E", command:"E", "tier1-artifact":"E", "tier2-embedded":"E", app:"E", deck:"E", marketing:"W", banner:"-", email:"-" },
  topbar:    { collateral:"E", command:"E", "tier1-artifact":"E", "tier2-embedded":"forbidden", app:"-", deck:"-", marketing:"-", banner:"-", email:"-" },
  eyebrow:   { collateral:"W", command:"W", "tier1-artifact":"W", "tier2-embedded":"-", app:"-", deck:"-", marketing:"W", banner:"-", email:"-" },
  hex:       { collateral:"E", command:"E", "tier1-artifact":"E", "tier2-embedded":"E", app:"E", deck:"E", marketing:"E", banner:"E", email:"-" },
  typefloor: { collateral:"E", command:"E", "tier1-artifact":"E", "tier2-embedded":"E", app:"E", deck:"E", marketing:"E", banner:"E", email:"-" },
  fonts:     { collateral:"-", command:"-", "tier1-artifact":"E", "tier2-embedded":"-", app:"-", deck:"-", marketing:"-", banner:"-", email:"-" },
};

const stripSvg      = (s) => s.replace(/<svg[\s\S]*?<\/svg>/gi, "");
const stripComments = (s) => s.replace(/<!--[\s\S]*?-->/g, "");

function styleText(html) {
  // hex + type checks scan CSS only: <style> blocks and style="" attributes.
  // Prose (#946-style issue refs, href anchors) is out of scope by construction.
  const blocks = [...html.matchAll(/<style[^>]*>([\s\S]*?)<\/style>/gi)].map((m) => m[1]);
  const attrs  = [...html.matchAll(/style="([^"]*)"/gi)].map((m) => m[1]);
  return blocks.concat(attrs).join("\n");
}

function lintFile(path) {
  const raw = readFileSync(path, "utf8");
  const errors = [], warns = [];

  const meta = raw.match(/<meta\s+name="celorus-surface"\s+content="([^"]+)"/i);
  if (!meta) {
    errors.push(`missing <meta name="celorus-surface"> declaration (one of: ${CLASSES.join("|")})`);
    return { errors, warns, surface: null };
  }
  const surface = meta[1];
  if (!CLASSES.includes(surface)) {
    errors.push(`unknown celorus-surface "${surface}" (expected one of: ${CLASSES.join("|")})`);
    return { errors, warns, surface };
  }
  if (surface === "email") return { errors, warns, surface }; // sanctioned inline-hex surface, own guide

  const html = stripComments(raw);
  const report = (rule, ok, msgMissing, msgForbidden) => {
    const level = RULES[rule][surface];
    if (level === "-") return;
    if (level === "forbidden") { if (ok) warns.push(msgForbidden); return; }
    if (!ok) (level === "E" ? errors : warns).push(msgMissing);
  };

  report("bridge",
    /prefers-color-scheme/.test(html) && /data-theme/.test(html),
    "theme bridge missing (prefers-color-scheme + data-theme stamp — SKILL.md step 3)");

  report("provenance",
    /Generated by\s*(<[^>]+>\s*)*Celorus/i.test(html),
    'provenance line missing ("Generated by Celorus · date · sources cited inline" — AGENTS.md rule 4)');

  report("topbar",
    /component-nav-topbar-bg|class="[^"]*\b(coll-topbar|topbar|brandbar)\b[^"]*"/.test(html),
    "charcoal topbar missing (standalone/tier-1 anatomy — COMPOSITION.md)",
    "host-chrome present on a tier-2 embedded surface (the host owns the window — PLUGIN_SURFACES.md)");

  // eyebrow rhythm: every <h2> should have an eyebrow-family element within the
  // preceding 600 chars (eyebrow | ew | h2-rule | .tag rule variants).
  if (RULES.eyebrow[surface] !== "-") {
    const body = stripSvg(html);
    let bare = 0;
    for (const m of body.matchAll(/<h2[\s>]/g)) {
      const before = body.slice(Math.max(0, m.index - 600), m.index);
      if (/class="[^"]*close/.test(before)) continue; // charcoal close bands open with a plain serif h2 by design
      if (!/class="[^"]*(eyebrow|\bew\b|h2-rule|dhead)[^"]*"/.test(before)) bare++;
    }
    report("eyebrow", bare === 0,
      `${bare} <h2> without an eyebrow opener nearby (sections open eyebrow → h2 — AGENTS.md rule 2)`);
  }

  // hex literals — CSS scope only, data: URIs removed first
  if (RULES.hex[surface] !== "-") {
    const css = styleText(html).replace(/url\(data:[^)]*\)/g, "");
    const hexes = [...new Set(css.match(/#[0-9A-Fa-f]{3,8}\b/g) || [])];
    report("hex", hexes.length === 0,
      `hex literal(s) in CSS: ${hexes.slice(0, 6).join(" ")} — bind to tokens (AGENTS.md rule 1)`);
  }

  // type floor — literal px font sizes in CSS (var()/clamp() minimums are out of scope)
  if (RULES.typefloor[surface] !== "-") {
    const css = styleText(html);
    const sizes = [...css.matchAll(/font-size\s*:\s*([\d.]+)px/g)].map((m) => parseFloat(m[1]));
    const tiny = sizes.filter((v) => v < 9);
    const small = sizes.filter((v) => v >= 9 && v < 10);
    if (tiny.length) errors.push(`font-size below 9px: ${[...new Set(tiny)].join(", ")}px — below any sanctioned floor`);
    if (small.length) warns.push(`font-size below 10px: ${[...new Set(small)].join(", ")}px — the token floor is 10px (fontSize-3xs); justify or raise`);
  }

  report("fonts",
    /data:font\/woff2/.test(raw),
    "tier-1 hosted artifact without embedded brand fonts (inline tokens/dist/embedded-fonts.css — CSP blocks font CDNs)");

  return { errors, warns, surface };
}

const files = process.argv.slice(2);
if (!files.length) {
  console.error("usage: node tools/composition-lint.mjs <file.html> [...]");
  process.exit(2);
}

let failed = 0;
for (const f of files) {
  const { errors, warns, surface } = lintFile(f);
  const tag = surface ? `[${surface}]` : "[no-surface]";
  if (errors.length === 0 && warns.length === 0) {
    console.log(`✓ ${f} ${tag}`);
  } else {
    console.log(`${errors.length ? "✗" : "△"} ${f} ${tag}`);
    for (const e of errors) console.log(`   ERROR  ${e}`);
    for (const w of warns) console.log(`   warn   ${w}`);
  }
  if (errors.length) failed++;
}
console.log(failed ? `\n${failed} file(s) FAILED composition lint.` : "\nComposition lint passed.");
process.exit(failed ? 1 : 0);
